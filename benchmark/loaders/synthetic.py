"""Dataset loader for synthetic benchmark data.

Loads pre-generated JSONL files created by the synthetic dataset generator.
"""

import json
from pathlib import Path

from benchmark.loaders.base import BenchmarkSample, DatasetLoader, FilterSpec


# Default path for generated synthetic dataset
DEFAULT_DATASET_PATH = Path(__file__).parent.parent / "synthetic" / "generated" / "swiss_benchmark.jsonl"


class SyntheticDatasetLoader(DatasetLoader):
    """Loader for synthetic PII benchmark dataset.

    Loads JSONL files generated by benchmark.synthetic.generate_dataset.

    Expected JSONL format:
        {
            "id": "synthetic_0001",
            "text": "Text with PII...",
            "ground_truth": [
                {"entity_type": "PERSON", "start": 13, "end": 24, "value": "Hans Mueller"}
            ],
            "metadata": {
                "locale": "de_CH",
                "domain": "banking",
                ...
            }
        }
    """

    def __init__(
        self,
        dataset_path: str | Path | None = None,
        split: str = "train",
        filters: FilterSpec | None = None,
    ):
        """Initialize the synthetic dataset loader.

        Args:
            dataset_path: Path to JSONL file. If None, uses default path.
            split: Dataset split (not used, kept for API compatibility).
            filters: Filter specification for loading specific subsets.
        """
        super().__init__(filters)
        self.dataset_path = Path(dataset_path) if dataset_path else DEFAULT_DATASET_PATH
        self._split = split

    def name(self) -> str:
        """Return the dataset name."""
        return "synthetic"

    def load(self, max_samples: int | None = None) -> list[BenchmarkSample]:
        """Load samples from the synthetic dataset.

        Args:
            max_samples: Maximum number of samples to load (None for all).

        Returns:
            List of BenchmarkSample objects.

        Raises:
            FileNotFoundError: If dataset file doesn't exist.
        """
        if not self.dataset_path.exists():
            raise FileNotFoundError(
                f"Synthetic dataset not found at: {self.dataset_path}\n"
                "Generate it first with:\n"
                "  python -m benchmark.synthetic.generate_dataset "
                "--templates ./benchmark/synthetic/templates/ "
                f"--output {self.dataset_path}"
            )

        samples = []

        with open(self.dataset_path, "r", encoding="utf-8") as f:
            for line in f:
                if not line.strip():
                    continue

                record = json.loads(line)

                # Apply filters
                if not self.filters.matches(record.get("metadata", {})):
                    continue

                sample = BenchmarkSample(
                    text=record["text"],
                    ground_truth=record["ground_truth"],
                    metadata=record.get("metadata", {}),
                )
                samples.append(sample)

                if max_samples and len(samples) >= max_samples:
                    break

        return samples

    def list_fields(self) -> dict[str, list[str] | str]:
        """List available fields in the dataset.

        Returns:
            Dict with 'fields' list and sample record.
        """
        fields = [
            "locale",
            "domain",
            "length_category",
            "pii_density",
            "tone",
            "structure_type",
            "pii_count",
            "pii_categories_used",
        ]

        info = {
            "fields": fields,
            "dataset": str(self.dataset_path),
        }

        # Try to get a sample record
        try:
            samples = self.load(max_samples=1)
            if samples:
                info["sample"] = samples[0].metadata
        except FileNotFoundError:
            info["error"] = "Dataset not generated yet. Run generate_dataset first."
        except Exception as e:
            info["error"] = str(e)

        return info


def register_loader():
    """Register the synthetic dataset loader.

    This function is called by the CLI to register the loader.
    """
    return SyntheticDatasetLoader
